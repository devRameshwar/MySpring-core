package in.employee;

public class EmployeeEntity {

	private Integer empid;
	private String empName;
	private String degination;
	private double sallry;

	public Integer getEmpid() {
		return empid;
	}

	public void setEmpid(Integer empid) {
		this.empid = empid;
	}

	public String getEmpName() {
		return empName;
	}

	public void setEmpName(String empName) {
		this.empName = empName;
	}

	public String getDegination() {
		return degination;
	}

	public void setDegination(String degination) {
		this.degination = degination;
	}

	public double getSallry() {
		return sallry;
	}

	public void setSallry(double sallry) {
		this.sallry = sallry;
	}

}
